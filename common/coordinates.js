/**
 * Represents coordinates in 3D space
 */
class Coordinates {
    /**
     * Creates a coordinates instance
     *
     * @param {number} x X position
     * @param {number} y Y position
     * @param {number} z Z position
     */
    constructor(x, y, z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}

export default Coordinates;

